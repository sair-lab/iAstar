class MapNoise(nn.Module):
    def __init__(self, beta = 0.5):
        super().__init__()
        self.beta = beta
    def forward(self, x):
        random_noise = torch.randn_like(x)
        noise = x * random_noise
        t = (1-self.beta)**(0.5)*x + self.beta * noise
        output = nn.Softmax(2)(t.view(*t.size()[:2], -1)).view_as(t)
        return output

class DiffusionCNN(nn.Module):
    def __init__(self, input_dim, depth=4, kernel_size = 3, stride = 1, padding = 1):
        super().__init__()
        self.model = self.construct_encoder(input_dim, depth, kernel_size, stride, padding)

    def construct_encoder(self, input_dim, encoder_depth, kernel_size, stride, padding) -> nn.Module:
        blocks = []
        channels = [input_dim, 32, 64, 128, 256] + [1]
        for i in range(len(channels) - 1): 
            blocks.append(nn.Conv2d(channels[i], channels[i + 1], kernel_size, stride, padding))
            blocks.append(MapNoise())
            blocks.append(nn.BatchNorm2d(channels[i + 1]))
            blocks.append(nn.ReLU())
        return nn.Sequential(*blocks[:-1])

